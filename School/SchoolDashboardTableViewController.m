#pragma mark forward to data details
-(void) addTapGestureToView:(UIView *)view
{
    view.userInteractionEnabled = YES;
    UITapGestureRecognizer *tapGesture = [[UITapGestureRecognizer alloc] initWithTarget:self action:@selector(forwardToViewDetails:)];
    [view addGestureRecognizer:tapGesture];
}

- (void) forwardToViewDetails:(UITapGestureRecognizer *)sender {
    
    UILabel *label = [sender.view.subviews objectAtIndex:1] ;
    
    if ([label.text isEqualToString:@"Location"]) {
        [self performSegueWithIdentifier:@"Location_Segue" sender:self];
    } else if([label.text isEqualToString:@"Photos"]) {
        [self performSegueWithIdentifier:@"Photos_Segue" sender:self];
    }
    
}

